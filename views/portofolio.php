        Saat lebaran tahun 2017 saya pergi ke Bandung,Jawa Barat. Waktu perjalanan dari Cirebon-bandung dengan kendaraan beroda 4 adalah 2 jam.<br> Saya pergi ke Bandug bersama keluarga saya beranggotakan 5 orang. Saya, papa, mama, kakak, dan juga sepupu saya.Saya pergi dari Cirebon-Bandung<br> sore-sore dan sampai ke Bandung malam-malam. Setelah saya sampai di Bandung saya langsung check in di Hotel bintang 5 Padma.Keesokan harinya, <br>saya langsung pergi ke tempat rekreasi Kampung Gajah. Di Kampung Gajah ada banyak sekali permainan seperti go-kart, perahu bebek, dll.<br> Di Kampung Gajah juga ada kafetaria yang makananya sangat enak-enak seperti nasi campur, sate ayam, dll. Setelah pergi ke Kampung <br>Gajah saya pergi ke Trans Studio Bandung. Di Trans Studio Bandung terdapat banyak wahana seperti halilintar, arum jeram, dll.<br> Setelah selesai bermain-main di Trans Studio Bandung saya pergi makan malam.Selesainya makan malam saya check out di Padma<br> hotel dan check in di hotel yang berada di Lembang bernama G.H Universal. Di G.H Universal terdapat banyak fasilitas seperti kolam renang, restoran, dll.<br>
        keesokan harinya saya pergi ke factory outlet. Setelah membeli oleh-oleh di factory outlet saya pergi ke Rumah Sosis untuk membeli oleh-oleh berupa makanan.<br> Setelah membeli oleh-oleh di rumah sosis saya pulang ke Jakarta menggunakan kendarran beroda 4. Saya sangat senang pergi ke Bandung.
                               
                             
